# Criação da lista com 10 nomes
nomes = ["Ana", "Bruno", "Carlos", "Diana", "Eduardo", "Fernanda", "Gabriel", "Helena", "Igor", "Juliana"]

# a) Mostrando apenas o primeiro elemento da lista
print(f"O primeiro elemento da lista é: {nomes[0]}")

# b) Mostrando apenas o 5° elemento da lista
print(f"O 5° elemento da lista é: {nomes[4]}")

# c) Mostrando somente o 2° e 3° elemento da lista
print(f"O 2° e 3° elementos da lista são: {nomes[1]} e {nomes[2]}")

# d) Mostrando os 6 primeiros elementos da lista
print(f"Os 6 primeiros elementos da lista são: {nomes[:6]}")

# e) Mostrando os 3 últimos elementos da lista
print(f"Os 3 últimos elementos da lista são: {nomes[-3:]}")
# Recebe o número inserido pelo usuário
n = int(input("Digite um número inteiro: "))

# Averigua se os números inseridos pelos usuários serão ímpares ou pares
if n % 2 == 0:
    print(f"O número {n} é par.") # Imprime caso o número for par
else:
    print(f"O número {n} é ímpar.") # Imprime caso o número for ímpar